from django import forms

class StressForm(forms.Form):
    age = forms.FloatField()
    gender = forms.IntegerField()
    social_media_usage = forms.IntegerField()
    dominant_platform = forms.IntegerField()
    check_freq = forms.IntegerField()
    pre_sleep_usage = forms.IntegerField()
    content_type = forms.IntegerField()
    sleep_latency = forms.IntegerField()
    total_sleep = forms.IntegerField()
    sleep_efficiency = forms.IntegerField()
    sleep_quality = forms.IntegerField()
    waso = forms.IntegerField()
    num_awakenings = forms.IntegerField()
    melatonin = forms.IntegerField()
    cortisol = forms.IntegerField()
    blue_light_exposure = forms.IntegerField()
    stress_level = forms.IntegerField()
    anxiety_depression = forms.IntegerField()
    restlessness = forms.IntegerField()
    interest_fluctuation = forms.IntegerField()
    relationship_status = forms.IntegerField()
    loneliness = forms.IntegerField()
